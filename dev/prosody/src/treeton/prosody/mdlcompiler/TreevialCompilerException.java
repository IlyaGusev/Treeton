package treeton.prosody.mdlcompiler;

public class TreevialCompilerException extends Exception {

	public TreevialCompilerException() {
		super();
	}

	public TreevialCompilerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TreevialCompilerException(String arg0) {
		super(arg0);
	}

	public TreevialCompilerException(Throwable arg0) {
		super(arg0);
	}
}
